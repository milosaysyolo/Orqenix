import { readFile } from "node:fs/promises";
import matter from "gray-matter";
import { hashString } from "../util/hash.js";
import type { SkillFile, SkillFrontmatter } from "../types/skill.js";

/**
 * Read a SKILL.md source file and parse its frontmatter.
 *
 * @param path - Absolute path to SKILL.md
 * @param name - Canonical skill name (must match directory)
 * @returns Parsed SkillFile with frontmatter, body, and content hash
 */
export async function readSkillSource(path: string, name: string): Promise<SkillFile> {
  const raw = await readFile(path, "utf-8");
  const parsed = matter(raw);
  return {
    name,
    sourcePath: path,
    frontmatter: parsed.data as SkillFrontmatter,
    body: parsed.content,
    contentHash: `blake3:${hashString(raw)}`,
  };
}

/**
 * Compile a source SkillFile into an OpenCode-compatible SKILL.md output.
 * OpenCode-recognized frontmatter fields only: name, description, license,
 * compatibility, metadata. Unknown fields are dropped. Metadata values are
 * coerced to strings per OpenCode spec.
 *
 * @param skill - Parsed SkillFile from readSkillSource
 * @param meta - Metadata including teamName, teamVersion, outputName
 * @returns OpenCode-compatible SKILL.md string with auto-generated marker
 */
export function compileSkillForOpenCode(
  skill: SkillFile,
  meta: { teamName?: string; teamVersion?: string; outputName?: string } = {},
): string {
  const markerLines = [
    "<!--",
    "  ⚠️  AUTO-GENERATED BY ORQENIX — DO NOT EDIT",
    `  Source: ${skill.sourcePath}`,
    meta.teamName ? `  Team:   ${meta.teamName} v${meta.teamVersion ?? "?"}` : "  Standalone skill",
    `  Synced: ${new Date().toISOString()}`,
    `  Hash:   ${skill.contentHash}`,
    "",
    "  Edit the source file instead, or run:",
    `    orqenix skill edit ${skill.name}`,
    "-->",
  ];

  const cleaned: SkillFrontmatter = {
    name: skill.frontmatter.name,
    description: skill.frontmatter.description,
  };
  if (skill.frontmatter.license) cleaned.license = String(skill.frontmatter.license);
  if (skill.frontmatter.compatibility) {
    cleaned.compatibility = String(skill.frontmatter.compatibility);
  }
  if (skill.frontmatter.metadata) {
    const md: Record<string, string> = {};
    for (const [k, v] of Object.entries(skill.frontmatter.metadata)) {
      md[k] = String(v);
    }
    cleaned.metadata = md;
  }

  const yaml = renderSkillYAML(cleaned);
  const body = skill.body.trimStart();
  return `${markerLines.join("\n")}\n---\n${yaml}\n---\n\n${body}`;
}

function renderSkillYAML(obj: SkillFrontmatter): string {
  const lines: string[] = [];
  lines.push(`name: ${quoteIfNeeded(obj.name)}`);
  lines.push(`description: ${quoteIfNeeded(obj.description)}`);
  if (obj.license) lines.push(`license: ${quoteIfNeeded(obj.license)}`);
  if (obj.compatibility) lines.push(`compatibility: ${quoteIfNeeded(obj.compatibility)}`);
  if (obj.metadata) {
    lines.push("metadata:");
    for (const [k, v] of Object.entries(obj.metadata)) {
      lines.push(`  ${k}: ${quoteIfNeeded(v)}`);
    }
  }
  return lines.join("\n");
}

function quoteIfNeeded(s: string): string {
  if (/[:#@`'"\n]/.test(s) || s.trim() !== s || s === "") return JSON.stringify(s);
  return s;
}
