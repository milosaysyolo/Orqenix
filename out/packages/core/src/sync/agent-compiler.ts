import { readFile } from "node:fs/promises";
import matter from "gray-matter";
import { hashString } from "../util/hash.js";
import type { AgentFile, AgentFrontmatter } from "../types/agent.js";

/**
 * Read an Orqenix source agent .md file and return a structured AgentFile.
 *
 * @param path - Absolute path to the agent source .md file
 * @param name - Canonical agent name
 * @returns Parsed AgentFile with frontmatter, body, and content hash
 */
export async function readAgentSource(path: string, name: string): Promise<AgentFile> {
  const raw = await readFile(path, "utf-8");
  const parsed = matter(raw);
  const frontmatter = parsed.data as AgentFrontmatter;
  return {
    name,
    sourcePath: path,
    frontmatter,
    body: parsed.content,
    contentHash: `blake3:${hashString(raw)}`,
  };
}

/**
 * Compile an Orqenix source AgentFile into an OpenCode-compatible .md output.
 * Adds a warning marker comment. Frontmatter passes through as YAML.
 *
 * @param agent - Parsed AgentFile from readAgentSource
 * @param meta - Metadata including teamName, teamVersion, outputName
 * @returns OpenCode-compatible agent .md string with auto-generated marker
 */
export function compileAgentForOpenCode(
  agent: AgentFile,
  meta: { teamName: string; teamVersion: string; outputName: string },
): string {
  const markerLines = [
    "<!--",
    "  ⚠️  AUTO-GENERATED BY ORQENIX — DO NOT EDIT",
    `  Source: ${agent.sourcePath}`,
    `  Team:   ${meta.teamName} v${meta.teamVersion}`,
    `  Synced: ${new Date().toISOString()}`,
    `  Hash:   ${agent.contentHash}`,
    "",
    "  Edit the source file instead, or run:",
    `    orqenix team edit ${meta.teamName} ${agent.name.replace(meta.teamName + "-", "")}`,
    "-->",
  ];

  const yaml = renderYAML(agent.frontmatter);
  const body = agent.body.trimStart();

  return `${markerLines.join("\n")}\n---\n${yaml}\n---\n\n${body}`;
}

/** Minimal YAML renderer for the subset we use (no anchors/tags). */
function renderYAML(obj: unknown, indent = 0): string {
  const pad = "  ".repeat(indent);
  if (obj === null || obj === undefined) return "null";
  if (typeof obj === "string") return needsQuotes(obj) ? JSON.stringify(obj) : obj;
  if (typeof obj === "number" || typeof obj === "boolean") return String(obj);
  if (Array.isArray(obj)) {
    if (obj.length === 0) return "[]";
    return obj
      .map((item) => {
        const r = renderYAML(item, indent + 1);
        if (typeof item === "object" && item !== null && !Array.isArray(item)) {
          return `${pad}-\n${r}`;
        }
        return `${pad}- ${r}`;
      })
      .join("\n");
  }
  if (typeof obj === "object") {
    const entries = Object.entries(obj as Record<string, unknown>);
    if (entries.length === 0) return "{}";
    return entries
      .map(([k, v]) => {
        if (v === null || v === undefined) return `${pad}${k}: null`;
        if (typeof v === "object" && !Array.isArray(v)) {
          return `${pad}${k}:\n${renderYAML(v, indent + 1)}`;
        }
        if (Array.isArray(v)) {
          if (v.length === 0) return `${pad}${k}: []`;
          return `${pad}${k}:\n${renderYAML(v, indent + 1)}`;
        }
        return `${pad}${k}: ${renderYAML(v, indent + 1)}`;
      })
      .join("\n");
  }
  return String(obj);
}

function needsQuotes(s: string): boolean {
  return /[:#@`'"\n]/.test(s) || s.trim() !== s || s === "";
}
